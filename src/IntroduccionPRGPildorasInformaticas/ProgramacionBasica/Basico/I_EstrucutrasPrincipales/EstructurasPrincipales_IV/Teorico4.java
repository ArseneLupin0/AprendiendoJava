    /* --> Constantes en java
     *
     * ¿Qué es una constante?
     *
     * - Espacio en la memoria del ordenador donde se almacenará un valor que no podrá cambiar durante
     *   la ejecución del programa.
     *
     * ¿Por qué hay que utilizar constantes?
     *
     * - Porque a la hora de crear un programa a veces surge la necesidad de guardar datos temporalmente
     *   que necesitarás utilizar en el futuro en ese mismo programa. Dichos datos deberán ser fijos.
     *
     * ¿Cómo se crea una constante en Java?
     *
     * - Utilizando la palabra clave final y a continuación especificando el tipo de dato que almacenará
     *   en su interior + el nombre de la constante = valor. Ej: final double a_pulgadas = 2,54;
     *
     * Recuerda el valor de una constante no podrá cambiar.
     *
     *
     * --> Operadores en Java
     *
     * - Aritméticos:
     *      · + = Suma
     *      · - = Resta
     *      · * = Multiplicación
     *      · / = División
     *      · ^ = Elevar
     *
     * - Lógicos, relaciones y booleanos:
     *      · >  = Mayor que
     *      · <  = Menor que
     *      · <> = Mayor que o menor que
     *      · != = Distinto que
     *      · == = Igual que
     *      · && = Y lógico
     *      · || = O lógico
     *
     * - Incremento y decremento:
     *      · ++ = Incremento
     *      · -- = Decremento
     *      · += nº = Incremento
     *      · -= nº = Decremento
     *
     * - Concatenación:
     *      · + = une o concatena
     */
